using System.Text;

namespace InfoProtection.Protection
{
    public static class Kuznechik
    {
        public static string KuznechikEncrypt(string text)
        {
            return $"{text} Зашифровано с помощью кузнечика" ; // Заглушка
        }
        public static string KuznechikDecrypt(string text)
        {
            return $"{text} Расшифровано с помощью кузнечика"; // Заглушка
        }
    }
}
