using System.Text;

namespace InfoProtection.Protection
{
    public static class RSA
    {
        public static string RsaEncrypt(string text)
        {
            return $"{text} Зашифровано с помощью рса"; // Заглушка
        }
        public static string RsaDecrypt(string text)
        {
            return $"{text} Расшифровано с помощью рса"; // Заглушка
        }

    }
}
